package com.app.emp;

public class Employee {
	// adding two number
	public int add(int a, int b) {
		int add=a + b;
		return add;
				
	}

	// multplication
	public int mull(int a, int b) {
		return a * b;
	}

	// devision
	public int div(int a, int b) {
		return a / b;
	}

	// sub
	public int sub(int a, int b) {
		return a - b;
	}
}
